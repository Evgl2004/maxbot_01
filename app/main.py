"""
Главный файл бота
=====================================================
Этот модуль отвечает за запуск и остановку бота,
инициализацию всех необходимых компонентов и обработку
жизненного цикла приложения. Он использует библиотеку maxapi
для взаимодействия с мессенджером MAX.
"""

import asyncio          # библиотека для асинхронного программирования
import sys              # системные функции, например, для выхода при ошибках

from loguru import logger  # красивое и удобное логирование

# Импортируем основные классы из maxapi для создания бота и диспетчера
from maxapi import Bot, Dispatcher

# Импортируем наши собственные модули (конфигурация, обработчики, база данных и т.д.)
from app.config import settings                 # настройки, загруженные из .env
from app.handlers import setup_routers          # функция, которая подключает все обработчики сообщений
from app.middlewares import setup_middlewares   # функция, которая подключает все промежуточные слои (middleware)
from app.database import db                      # объект для работы с базой данных (PostgreSQL)
from app.services import iiko_service            # сервис для интеграции с iiko (лояльность, карты)


async def setup_bot() -> tuple[Bot, Dispatcher]:
    """
    🔧 Настройка бота и диспетчера (роутера).

    Эта функция создаёт экземпляры бота и диспетчера,
    подключает middleware и роутеры, и возвращает их.
    Именно здесь происходит инициализация всех компонентов,
    необходимых для работы бота.

    Возвращает:
        tuple[Bot, Dispatcher]: кортеж из двух объектов:
            - bot: объект для отправки запросов к API MAX
            - dp:  объект для регистрации обработчиков событий
    """
    logger.info("🤖 Инициализация MAX бота...")

    # ------------------------------------------------------------
    # Создаём экземпляр бота
    # ------------------------------------------------------------
    # Конструктор Bot принимает только один обязательный параметр – токен,
    # полученный от @masterbot. Никаких дополнительных настроек сессии
    # не требуется (в отличие от aiogram).
    bot = Bot(token=settings.bot_token)
    logger.info("✅ Экземпляр бота создан")

    # ------------------------------------------------------------
    # Создаём экземпляр диспетчера
    # ------------------------------------------------------------
    # Диспетчер – это главный объект, который управляет всеми обработчиками.
    # В maxapi хранилище состояний (FSM) встроено прямо в диспетчер,
    # поэтому нам не нужно создавать отдельный RedisStorage, как в aiogram.
    dp = Dispatcher()
    logger.info("✅ Экземпляр диспетчера создан")

    # ------------------------------------------------------------
    # Подключаем middleware (промежуточные обработчики)
    # ------------------------------------------------------------
    # Middleware позволяют выполнять код до и после вызова основного обработчика.
    # Например, логирование, сохранение пользователя в БД, проверка прав доступа.
    # Функция setup_middlewares будет реализована в пакете app/middlewares.
    setup_middlewares(dp)

    # ------------------------------------------------------------
    # Подключаем все роутеры (обработчики команд и сообщений)
    # ------------------------------------------------------------
    # Роутеры группируют хендлеры по функциональному признаку (например, регистрация,
    # меню, админка). Функция setup_routers будет реализована в пакете app/handlers.
    setup_routers(dp)

    return bot, dp


async def on_startup(bot: Bot) -> None:
    """
    🚀 Действия, выполняемые при запуске бота (перед началом поллинга).

    Здесь мы инициализируем базу данных, подключаемся к сервису iiko,
    а также выводим информацию о боте в логи. Если какой-то из этапов
    завершается ошибкой, программа завершается с кодом 1.

    Аргументы:
        bot (Bot): экземпляр бота, который будет использоваться
                   для получения информации о себе (get_me).
    """
    # ------------------------------------------------------------
    # 1. Инициализация базы данных
    # ------------------------------------------------------------
    try:
        # Создаём таблицы (если их ещё нет) и обновляем статистику
        await db.create_tables()
        await db.update_bot_stats()
        logger.info("✅ База данных успешно инициализирована")
    except Exception as e:
        # В случае любой ошибки логируем и завершаем процесс
        logger.error(f"❌ Не удалось инициализировать базу данных: {e}")
        sys.exit(1)

    # ------------------------------------------------------------
    # 2. Инициализация клиента iiko
    # ------------------------------------------------------------
    try:
        # Подключаемся к API iiko (создаём сессию и т.д.)
        await iiko_service.init_iiko_client()
        logger.info("✅ Клиент iiko успешно инициализирован")
    except Exception as e:
        logger.error(f"❌ Не удалось инициализировать клиент iiko: {e}")
        sys.exit(1)

    # ------------------------------------------------------------
    # 3. Получение информации о самом боте и вывод в логи
    # ------------------------------------------------------------
    # Метод get_me() возвращает объект, содержащий поля:
    #   - username: имя бота (без @)
    #   - user_id:  числовой идентификатор
    #   - first_name, last_name и т.д.
    bot_info = await bot.get_me()
    logger.info(f"🚀 Бот @{bot_info.username} успешно запущен!")
    logger.info(f"🏠 Окружение: {settings.env}")


async def on_shutdown(bot: Bot) -> None:
    """
    🛑 Действия, выполняемые при остановке бота.

    Здесь мы закрываем все открытые соединения (iiko, сессию aiohttp),
    чтобы избежать утечек ресурсов. Логируем завершение работы.

    Аргументы:
        bot (Bot): экземпляр бота, у которого нужно закрыть сессию.
    """
    logger.info("🛑 Бот завершает работу...")

    # ------------------------------------------------------------
    # Закрываем клиент iiko (освобождаем соединения)
    # ------------------------------------------------------------
    await iiko_service.close_iiko_client()
    logger.info("🛑 Клиент iiko закрыт")

    # ------------------------------------------------------------
    # Закрываем сессию aiohttp, которую использует bot
    # ------------------------------------------------------------
    await bot.close_session()


async def main() -> None:
    """
    🎯 Главная асинхронная функция приложения.

    Последовательность действий:
      1. Настраивается логирование (цветной вывод с временем).
      2. Создаются бот и диспетчер через setup_bot().
      3. Выполняются действия при старте (on_startup).
      4. Запускается бесконечный цикл получения обновлений (polling).
      5. При возникновении исключений или нажатии Ctrl+C,
         выполняются действия при остановке (on_shutdown).
    """
    # ------------------------------------------------------------
    # 1. Настройка логирования (через loguru)
    # ------------------------------------------------------------
    # Убираем стандартный вывод (чтобы заменить своим форматом)
    logger.remove()
    # Добавляем свой формат с цветами, временем, уровнем и именем функции
    logger.add(
        sys.stdout,
        level=settings.log_level,
        format="<green>{time:YYYY-MM-DD HH:mm:ss}</green> | "
               "<level>{level: <8}</level> | "
               "<cyan>{name}</cyan>:<cyan>{function}</cyan>:<cyan>{line}</cyan> - "
               "<level>{message}</level>",
        colorize=True
    )

    logger.info("🎯 Запуск MAX бота с maxapi...")

    # ------------------------------------------------------------
    # 2. Создание бота и диспетчера
    # ------------------------------------------------------------
    bot, dp = await setup_bot()

    # ------------------------------------------------------------
    # 3. Выполнение стартовых действий
    # ------------------------------------------------------------
    await on_startup(bot)

    # ------------------------------------------------------------
    # 4. Запуск поллинга (основной цикл)
    # ------------------------------------------------------------
    try:
        logger.info("🔄 Запуск поллинга...")
        # start_polling – это бесконечный цикл, который будет работать,
        # пока его не прервать (например, по Ctrl+C).
        await dp.start_polling(bot)
    except KeyboardInterrupt:
        # Если пользователь нажал Ctrl+C в терминале
        logger.info("👋 Бот остановлен пользователем")
    except Exception as e:
        # Любая другая непредвиденная ошибка
        logger.error(f"💥 Непредвиденная ошибка: {e}")
    finally:
        # ------------------------------------------------------------
        # 5. Завершающие действия (выполняются в любом случае)
        # ------------------------------------------------------------
        await on_shutdown(bot)


if __name__ == "__main__":
    # Точка входа в программу: запускаем асинхронную функцию main()
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        # Дополнительная защита от Ctrl+C на уровне верхнего вызова
        logger.info("👋 Приложение завершено пользователем")
