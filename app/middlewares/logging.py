"""
Middleware для логирования входящих событий
=================================================
Этот модуль содержит класс LoggingMiddleware, который перехватывает все входящие события
(сообщения, нажатия на кнопки) и записывает информацию о них в лог с помощью loguru.
Middleware добавляется для диспетчера в файле middlewares/__init__.py.
"""

from loguru import logger
from maxapi.filters.middleware import BaseMiddleware
from maxapi.types import MessageCreated, MessageCallback


class LoggingMiddleware(BaseMiddleware):
    """
    📝 Класс промежуточного обработчика для логирования.

    Наследуется от BaseMiddleware из maxapi и переопределяет метод __call__,
    который вызывается для каждого события до того, как оно попадёт основной обработчик.

    Задачи:
    - Для событий типа MessageCreated (новое сообщение) логирует ID отправителя,
      его имя и начало текста (до 50 символов).
    - Для событий типа MessageCallback (нажатие на inline-кнопку) логирует
      ID отправителя, имя и payload кнопки.
    - Все остальные события игнорируются (но можно расширить при необходимости).
    """

    async def __call__(self, handler, event, data):
        """
        Основной метод, вызываемый диспетчером для каждого события.

        Аргументы:
            handler: следующий обработчик в цепочке (функция или другой middleware).
                     Его обязательно нужно вызвать с теми же аргументами, чтобы
                     продолжить обработку события
            event: объект события, который пришёл от API MAX. Это может быть
                     MessageCreated, MessageCallback или другой тип
            data: словарь с дополнительными данными, переданными от предыдущих
                     middleware. Сюда входит контекст FSM и другие данные

        Возвращает:
            Результат вызова следующего обработчика.
        """
        # ------------------------------------------------------------------
        # 1. Обработка события "новое сообщение" (MessageCreated)
        # ------------------------------------------------------------------
        if isinstance(event, MessageCreated):
            # Извлекаем объект пользователя (отправителя) из события
            user = event.from_user
            if user:
                # В maxapi у объекта User нет поля id, используется user_id
                # Используем getattr для безопасного доступа (если поле отсутствует)
                user_id = getattr(user, 'user_id', 'неизвестно')
                user_name = getattr(user, 'name', 'Неизвестно')
                # Получаем текст сообщения (первые 50 символов для краткости)
                text = event.message.body.text[:50] if event.message.body.text else 'нет текста'
                # Записываем в лог с эмодзи 📥
                logger.info(f"📥 Сообщение от {user_id} ({user_name}): '{text}'")

        # ------------------------------------------------------------------
        # 2. Обработка события "нажатие на callback-кнопку" (MessageCallback)
        # ------------------------------------------------------------------
        elif isinstance(event, MessageCallback):
            user = event.from_user
            if user:
                user_id = getattr(user, 'user_id', 'неизвестно')
                user_name = getattr(user, 'name', 'Неизвестно')
                # Извлекаем payload кнопки (данные, которые были указаны при создании)
                payload = event.callback.payload
                # Логируем с эмодзи 🔘
                logger.info(f"🔘 Callback от {user_id} ({user_name}): '{payload}'")

        # ------------------------------------------------------------------
        # 3. Другие типы событий можно добавить позже, если потребуется
        # ------------------------------------------------------------------

        # После завершения своей работы обязательно вызываем следующий обработчик
        return await handler(event, data)
